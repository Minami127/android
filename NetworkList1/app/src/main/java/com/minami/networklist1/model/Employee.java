package com.minami.networklist1.model;

public class Employee {

    public int id;
    public String name;
    public int salary;
    public int age;
    public String profileImage;


    public Employee extents (String name, int salary, int age) {
        this.id = id;
        this.name = name;
        this.salary = salary;
        this.age = age;
        this.profileImage = profileImage;
    }
}
